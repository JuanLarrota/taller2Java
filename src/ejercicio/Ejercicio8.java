package ejercicio;

public class Ejercicio8 {

    private int m;
    private int n;

    private int[][] matriz = new int[m][n];

    public int getM() {
        return this.m;
    }

    public void setM(int m) {
        this.m = m;
    }

    public int getN() {
        return this.n;
    }

    public void setN(int n) {
        this.n = n;
    }

    public int[][] getMatriz() {
        return this.matriz;
    }

    public void setMatriz(int[][] matriz) {
        this.matriz = matriz;
    }

}
